int mul(int x, int y) {
    return x * y;
}

int edv(int x, int y) {
    return x / y;
}

int add(int x, int y) {
    return x + y;
}

int sub(int x, int y) {
    return x - y;
}

int eql(int x, int y) {
    return x == y;
}

int neq(int x, int y) {
    return x != y;
}

int sup(int x, int y) {
    return x > y;
}

int inf(int x, int y) {
    return x < y;
}
